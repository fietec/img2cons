char* shift_args(int* argc, char*** argv)
{
    assert(*argc > 0);
    char* result = **argv;
    *argc -= 1;
    *argv += 1;
    return result;
}

int get_distance(int index, int r, int g, int b){
    int dr = r - colors256[index][0];
    int dg = g - colors256[index][1];
    int db = b - colors256[index][2];
    return dr*dr + dg*dg + db*db;
}

int get_print_color_index(int r, int g, int b)
{
    int index = 0;
    for (int i=0; i<256; ++i){
        if (get_distance(i, r, g, b) < get_distance(index, r, g, b)){
            index = i;
        }
    }
    return index;
}

void print_usage(void)
{
    printf("img2cons\n");
    printf("Basic Usage:    img2cons <file1> <file2> <file3>...\n");
    printf("Specific sizes: img2cons -w 16 <file1> <file2> -w 69 <file3>\n");
    printf("    -w:         sets the width of the image, the height remains proportional\n");
    printf("                The default value is %d pixels.\n", DEF_IMG_SIZE);
    printf("    --help:     prints this output\n");
}

int main(int argc, char** argv)
{
    shift_args(&argc, &argv);
    if (argc == 0){
        print_usage();
        exit(1);
    }
    int res_width = DEF_IMG_SIZE;
    while (argc > 0){
        const char* flag = shift_args(&argc, &argv);
        if (strcmp(flag, "--help") == 0){
            print_usage();
            exit(0);
        }
        if (strcmp(flag, "-w") == 0){
            int w = atoi(shift_args(&argc, &argv));
            if (w == 0){
                fprintf(stderr, "[ERROR] Please provide a number after '-w'!\n");
                exit(1);
            }
            res_width = w;
            continue;
        }

        int width, height;
        uint32_t* pixels = (uint32_t*) stbi_load(flag, &width, &height, NULL, 4);
        if (pixels == NULL){
            fprintf(stderr, "[ERROR] Failed to load file %s!\n", flag);
            print_usage();
            exit(1);
        }

        int res_height = res_width*height/width;

        uint32_t* res_pixels = (uint32_t*) calloc(res_width*res_height, sizeof(uint32_t));
        assert(res_pixels != NULL && "Out of memory!");

        stbir_resize_uint8_srgb( (unsigned char *) pixels, width, height, sizeof(*pixels) * width,
                              (unsigned char *) res_pixels, res_width, res_height, sizeof(*res_pixels)*res_width,
                              STBIR_RGBA);
        for (int y=0; y<res_height; ++y){
            for (int x=0; x<res_width; ++x){
                uint32_t pixel = res_pixels[y*res_width + x];
                int r = (pixel >> 8*0) & 0xFF;
                int g = (pixel >> 8*1) & 0xFF;
                int b = (pixel >> 8*2) & 0xFF;
                int a = (pixel >> 8*3) & 0xFF;

                r = r*a/255;
                g = g*a/255;
                b = b*a/255;
                printf("\e[48;5;%dm  ", get_print_color_index(r, g, b));
            }
            printf("\e[0m\n");
        }
        printf("\e[0m\n");

        free(res_pixels);
        stbi_image_free(pixels);
        res_width = DEF_IMG_SIZE;
    }
    return 0;
}